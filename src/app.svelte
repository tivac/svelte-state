<script>
    import { Machine } from "xstate";

    const machine = Machine({
        key: 'light',
        initial: 'green',
        states: {
            green: {
                on: {
                    TIMER: 'yellow',
                }
            },
            yellow: {
                on: {
                    TIMER: 'red',
                }
            },
            red: {
                on: {
                    TIMER: 'green',
                }
            }
        }
    });

    let state = machine.initialState;

    let next = machine.transition(state, "TIMER");

    console.log(next);

    next = machine.transition(next, "TIMER");

    console.log(next);
</script>
